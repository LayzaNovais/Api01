

export function somar (a, b){
    return a + b;
}

export function dobro (a){
    return a * 2;

}

export function media (n1,n2,n3){
    let a = n1+n2+n3;
    let b = a/3;
    return b;
}

export function temperatura (temp) {
    let a = temp >= 37.5;
    return a;
}

export function corPrimaria (cor) {
    let a = cor == "amarelo" || cor == "vermelho" || cor== "azul";
    return a;
}





